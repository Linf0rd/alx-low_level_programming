/*
 * _islower - entry point
 * description: checks if a character is lowercase
 * c: the integer value it receives
 * return: 1 if true, 0 if false.
 */
int _islower(int C)
{
int i = 'a';

for (i = 'a'; i <= 'z'; i++)
{
/* assign value to int c */
if (c == i)
{
return (1);
}
}
return (0);
}
